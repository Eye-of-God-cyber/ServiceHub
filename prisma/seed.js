// ─────────────────────────────────────────────────────────────────────────────
// STEP 0 — CLEAR (reverse dependency order, safe to re-run)
// ─────────────────────────────────────────────────────────────────────────────
async function clearFoundationData() {
  // Clear in strict reverse-FK order.
  // Catalog tables (2D) must be cleared BEFORE identity tables (2C) because:
  //   • provider_services FK → provider_profiles (Cascade) AND → services (Restrict)
  //   • services FK → service_categories (Restrict)
  // If we deleted provider_profiles first, provider_services would cascade-delete,
  // but services would still block service_categories deletion.
  // Explicit order prevents any FK violation.

  // ── Support & Disputes tables (2I) ─────────────────────────────────────────
  await prisma.disputeMessage.deleteMany();
  await prisma.dispute.deleteMany();

  // ── Notifications System tables (2H) ───────────────────────────────────────
  await prisma.notification.deleteMany();

  // ── Reviews System tables (2G) ─────────────────────────────────────────────
  await prisma.reviewReply.deleteMany();
  await prisma.review.deleteMany();

  // ── Financial System tables (2F) ───────────────────────────────────────────
  await prisma.payment.deleteMany();
  await prisma.walletTransaction.deleteMany();
  await prisma.wallet.deleteMany();
  await prisma.couponUsage.deleteMany();
  await prisma.coupon.deleteMany();

  // ── Booking System tables (2E) ─────────────────────────────────────────────
  await prisma.bookingStatusHistory.deleteMany();
  await prisma.booking.deleteMany();

  // ── Catalog tables (2D) ────────────────────────────────────────────────────
  await prisma.providerTimeOff.deleteMany();
  await prisma.providerAvailability.deleteMany();
  await prisma.providerService.deleteMany();
  await prisma.service.deleteMany();
  await prisma.serviceCategory.deleteMany();

  // ── Identity tables (2C) ───────────────────────────────────────────────────
  await prisma.providerDocument.deleteMany();
  await prisma.providerProfile.deleteMany();
  await prisma.address.deleteMany();
  await prisma.userProfile.deleteMany();
  await prisma.userRole.deleteMany();
  await prisma.user.deleteMany();
  await prisma.role.deleteMany();
  console.log('  ✓ All seed data cleared (catalog + identity)');
}

