async function seedReviews() {
  const completedBookings = await prisma.booking.findMany({
    where: { status: 'COMPLETED' },
    include: { provider: true }
  });

  // We want ~10-12 reviews out of 15 completed bookings. 
  // We'll shuffle and pick 12.
  const shuffled = completedBookings.sort(() => 0.5 - Math.random());
  const selectedBookings = shuffled.slice(0, 12);

  const reviewComments = [
    { rating: 5, comment: "Excellent service. Very professional." },
    { rating: 5, comment: "Arrived on time and finished quickly. Highly recommended." },
    { rating: 5, comment: "Very professional technician, knew exactly what to do." },
    { rating: 5, comment: "Fantastic experience! The problem was fixed in no time." },
    { rating: 5, comment: "Satisfied with the overall experience." },
    { rating: 4, comment: "Good work but arrived slightly late. Otherwise great." },
    { rating: 4, comment: "Service was good, but the price could be a little better." },
    { rating: 4, comment: "Very polite and helpful. Solved the issue." },
    { rating: 4, comment: "Would definitely book again." },
    { rating: 3, comment: "The issue was fixed, but it took much longer than expected." },
    { rating: 3, comment: "Average experience. The professional was okay but left a bit of a mess." }
  ];

  const replyComments = [
    "Thank you for your valuable feedback. We're glad you liked the service!",
    "We appreciate your review. Hoping to serve you again.",
    "Sorry about the delay. We'll improve next time.",
    "Thank you for choosing us!",
    "We're glad you had a good experience. Let us know if you need any further assistance."
  ];

  let reviewCount = 0;
  let replyCount = 0;

  for (const b of selectedBookings) {
    // Pick a random review template based on probabilities
    const rand = Math.random();
    let template;
    if (rand < 0.6) {
      template = reviewComments.filter(r => r.rating === 5)[Math.floor(Math.random() * 5)];
    } else if (rand < 0.9) {
      template = reviewComments.filter(r => r.rating === 4)[Math.floor(Math.random() * 4)];
    } else {
      template = reviewComments.filter(r => r.rating === 3)[Math.floor(Math.random() * 2)];
    }

    const reviewDate = new Date(b.completedAt.getTime() + 1000 * 60 * 60 * 24); // 1 day after completion

    const review = await prisma.review.create({
      data: {
        bookingId: b.id,
        customerId: b.customerId,
        providerId: b.providerId,
        rating: template.rating,
        comment: template.comment,
        createdAt: reviewDate,
        updatedAt: reviewDate
      }
    });
    reviewCount++;

    // 50% chance of reply
    if (Math.random() < 0.5) {
      const replyTemplate = replyComments[Math.floor(Math.random() * replyComments.length)];
      const replyDate = new Date(reviewDate.getTime() + 1000 * 60 * 60 * 5); // 5 hours after review

      await prisma.reviewReply.create({
        data: {
          reviewId: review.id,
          providerId: b.providerId,
          comment: replyTemplate,
          createdAt: replyDate,
          updatedAt: replyDate
        }
      });
      replyCount++;
    }
  }

  // Update Provider ratings
  const providers = await prisma.providerProfile.findMany();
  for (const p of providers) {
    const reviews = await prisma.review.findMany({ where: { providerId: p.id } });
    if (reviews.length > 0) {
      const totalRatings = reviews.reduce((sum, r) => sum + r.rating, 0);
      const avgRating = totalRatings / reviews.length;
      await prisma.providerProfile.update({
        where: { id: p.id },
        data: {
          totalReviews: reviews.length,
          avgRating: parseFloat(avgRating.toFixed(2))
        }
      });
    }
  }

  console.log(`  ✓ ${reviewCount} Reviews generated for completed bookings`);
  console.log(`  ✓ ${replyCount} Replies generated by providers`);
}

