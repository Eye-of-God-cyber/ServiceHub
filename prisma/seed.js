// STEP 3 — USER ROLES
// Depends on: Users, Roles
// Junction table — enforces @@unique([userId, roleId]) constraint.
// ─────────────────────────────────────────────────────────────────────────────
async function seedUserRoles({ admin, customers, providers }, { adminRole, customerRole, providerRole }) {
  const data = [
    { userId: admin.id, roleId: adminRole.id },
    ...customers.map((c) => ({ userId: c.user.id, roleId: customerRole.id })),
    ...providers.map((p) => ({ userId: p.user.id, roleId: providerRole.id })),
  ];

  // Using createMany — we don't need the returned IDs for this table.
  await prisma.userRole.createMany({ data });
  console.log(`  ✓ ${data.length} UserRoles assigned`);
}

